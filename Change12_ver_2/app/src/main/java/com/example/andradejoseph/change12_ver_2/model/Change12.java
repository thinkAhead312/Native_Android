public class Change12 {

    protected String change12_id;
    protected String wave_num;
    protected String start_date;
    protected String end_date;


    public String getChange12_id() {
        return change12_id;
    }

    public void setChange12_id(String change12_id) {
        this.change12_id = change12_id;
    }

    public String getWave_num() {
        return wave_num;
    }

    public void setWave_num(String wave_num) {
        this.wave_num = wave_num;
    }

    public String getStart_date() {
        return start_date;
    }

    public void setStart_date(String start_date) {
        this.start_date = start_date;
    }

    public String getEnd_date() {
        return end_date;
    }

    public void setEnd_date(String end_date) {
        this.end_date = end_date;
    }


}
